const numDoMes = 2

switch(numDoMes) {
  case 1:
    console.log("Janeiro")
  break
  case 2:
    console.log("Fevereiro")
  break
  case 3:
    console.log("Março")
  break
  case 4:
    console.log("Abril")
  break
  case 5:
    console.log("Maio")
  break
  default:
    console.log("Mês Inexistente")
  break
}

